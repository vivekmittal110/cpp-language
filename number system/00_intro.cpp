// number system 
// it represents a number using different forms using different base values

// Decimal number system
// base value -: 10
// 0-9 digits and power of 10
// 520(base 10) = 5 * 10^2 + 2 * 10^1 + 0 * 10^0

// Binary number system
// base value -: 2
// 0,1 digits, power of 2
// 1010(base 2) = 1 * 2^3 + 0 * 2^2 + 1 * 2^1 + 0 * 2^0 = 10(base 10)

// Hexadecimal number system
// base value -: 16
// 0-9, 10-A, 11-B, 12-C, 13-D, 14-E, powers of 16
// 4C5(base 16) = 4 * 16^2 + 12 * 16^1 + 5 * 16^0